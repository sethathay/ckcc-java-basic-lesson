package week1;

public class Division {
	public static void main(String[] args) {
		double a = 5.0 / 2.0;
		System.out.println(a);
		int b = 4 / 2;
		System.out.println(b);
		int c = 5 / 2;
		System.out.println(c);
		double d = 5 / 2;
		System.out.println(d);
		
		double x = 3 / 2 + 1;
		System.out.println(x);
		
		double y = 3 / (2 + 1);
		System.out.println(y);
	}
}
