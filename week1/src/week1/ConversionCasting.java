package week1;

public class ConversionCasting {
	public static void main(String[] args) {
		int a = 2; // a = 2
		System.out.println(a);
		double b = 2; // b = 2.0 (Implicit)
		System.out.println(b);
		int c = (int)18.7; // 18
		System.out.println(c);
		double d = 2 / 3; // d = 0.0
		System.out.println(d);
		double e = (double) 2 / 3; // e=0.666
		System.out.print(e);
	}
}
