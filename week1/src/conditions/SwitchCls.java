package conditions;

public class SwitchCls {

	public static void main(String[] args) {
		int i = 2;
		switch(i) {
			case 1:
				System.out.println("Case 1");
				break;
			case 2:
				System.out.println("Case 2");
				break;
			case 3:
				System.out.println("Case 3");
				break;
			case 4:
				System.out.println("Case 4");
				break;
			default:
				System.out.println("Default");
		}
	}
	
}
